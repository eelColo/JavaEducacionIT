package ej2;

public class Operaciones {
	public static void suma(int valA, int valB) {
		
		int resultado = valA + valB;
		
		System.out.println(resultado);
	}
	public static void resta(int valA, int valB) {
		
		int resultado = valA - valB;
		
		System.out.println(resultado);
	}
	public static void multiplicar(int valA, int valB) {
		
		int resultado = valA * valB;
		
		System.out.println(resultado);
	}
	public static void division(int valA, int valB) {
		
		
		int resultado = valA / valB;
		System.out.println(resultado);
		
	}
}
